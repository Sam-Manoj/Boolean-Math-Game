public class LEVEL1 {
    public static int or_op(int a, int b) {
        return a | b;
    }

    public static int and_op(int a, int b) {
        return a & b;
    }

    public static int bt_not_op(int a) {
        return ~a;
    }

    public static int exor_op(int a, int b) {
        return a ^ b;
    }
}
